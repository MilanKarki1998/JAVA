public class UnaryOperators {
    public static void main(String[] args){
        int a=5;
        int b=10;
        int c=0,d=0,e=0,f=0;
        c=++a;
        d=a++;
        e=--b;
        f=b--;
        System.out.println("The value of c(++a)= "+c);
        System.out.println("The value of d(a++)= "+d);
        System.out.println("The value of e(--b)= "+e);
        System.out.println("The value of f(b--)= "+f);
        System.out.println("Arun Shahi Sec=A Roll no.:5");
    }
    
}
